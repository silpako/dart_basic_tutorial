void main() {
// 10 silpa
  print("----------------");
  for (int count = 1; count <= 10; count++) {
    print("Silpa");
  }

  ///loop working
  /// 1.count = 1 ; 1<=10 ;(true) count++ = 2
  /// 2.count = 2 ; 2<=10 ;(true) count++ = 3
  /// 3.count = 3 ; 3<=10 ;(true) count++ = 4
  /// --------------------------------------
  /// 10.count = 10 ; 10<=10 ;(true) count++ = 11
  /// count = 11 ; 11<=10 ;(false) EXIT from loop

// 1 to 10
  print("----------------");
  for (int i = 1; i <= 10; i++) {
    print(i);
  }
}
