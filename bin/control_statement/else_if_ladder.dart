void main() {
  String size = "xs";
  if (size == 'S') {
    print("Shirt size is S");
  } else if (size == "M") {
    print("Shirt size is M");
  } else if (size == "L") {
    print("Shirt size is L");
  } else if (size == "XL") {
    print("Shirt size is XL");
  } else {
    print("size is not available");
  }
}
