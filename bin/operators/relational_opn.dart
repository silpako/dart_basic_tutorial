void main() {
  // relational operators : > , < , >=, <=, ==, !=
  int a = 124;
  int b = 40;
  print("a > b -> ${a > b}"); // true
  print("a < b -> ${a < b}"); // false
  print("a >= b -> ${a >= b}"); // true
  print("a <= b -> ${a <= b}"); // false
  print("a == b -> ${a == b}"); // false
  print("a != b -> ${a != b}"); // true
}
