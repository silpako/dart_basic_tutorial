void main() {
  // assignment operators : =, +=, -=, /=, *=, %= , ~/=
  int a = 124;
  int b = 40;
  print("a  = b -> ${a = b}"); // a=b => a=40
  print("a += b -> ${a += b}"); // a = a+b = 40+40 = 80
  print("a -= b -> ${a -= b}"); // a = a-b = 80-40 = 40
  print("a *= b -> ${a *= b}"); // a = a*b = 40*40 =1600
  // print("a /= b = ${a /= b}");
  print("a %= b -> ${a %= b}");
  print("a  ~/= b -> ${a ~/= b}");
}
